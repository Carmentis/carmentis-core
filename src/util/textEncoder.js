let encoder = new TextEncoder();
let decoder = new TextDecoder();

// ============================================================================================================================ //
//  encode()                                                                                                                    //
// ============================================================================================================================ //
export function encode(str) {
  return encoder.encode(str);
}

// ============================================================================================================================ //
//  decode()                                                                                                                    //
// ============================================================================================================================ //
export function decode(array) {
  return decoder.decode(array);
}
