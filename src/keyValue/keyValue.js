const cache = {};

// ============================================================================================================================ //
//  get()                                                                                                                       //
// ============================================================================================================================ //
export async function get(table, key) {
  return cache[table + "|" + key];
}

// ============================================================================================================================ //
//  set()                                                                                                                       //
// ============================================================================================================================ //
export async function set(table, key, value) {
  cache[table + "|" + key] = value;
}
